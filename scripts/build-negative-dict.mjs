import { writeFileSync } from 'fs'
import { fileURLToPath } from 'url'
import { resolve, dirname } from 'path'

const __dirname = dirname(fileURLToPath(import.meta.url))
const OUT = resolve(__dirname, '../src/lib/knuNegativeWords.ts')

console.log('KnuSentiLex 데이터 다운로드 중...')
const res = await fetch(
  'https://raw.githubusercontent.com/park1200656/KnuSentiLex/master/KnuSentiLex/data/SentiWord_info.json'
)
if (!res.ok) throw new Error(`HTTP ${res.status}`)

const data = await res.json()

const words = [
  ...new Set(
    data
      .filter((e) => e.polarity === '-2')
      .map((e) => e.word_root)
      .filter((w) => w && w.length >= 2)
  ),
].sort()

const content = `// Auto-generated by scripts/build-negative-dict.mjs
// Source: KnuSentiLex (Kangwon National University Sentiment Lexicon)
// Polarity: -2 (very negative), ${words.length} entries
export const KNU_NEGATIVE_WORDS: string[] = ${JSON.stringify(words, null, 2)}
`

writeFileSync(OUT, content, 'utf8')
console.log(`완료: ${words.length}개 단어 → ${OUT}`)
